package com.mf.lightcontrol.model.control;

public class DeviceModel {
    private String name;
    private String ip;

    public DeviceModel(String name, String ip) {
        this.name = name;
        this.ip = ip;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }
}
